/* 첫 번째 전체 메뉴 */
// 메뉴에 호버 시
$(function () {
  $('.menu1').mouseenter(function () {
    $('.menu01').stop().slideDown();
  });
});

// 메뉴에 아웃 시
$(function () {
  $('.menu1').mouseleave(function () {
    $('.menu01').stop().slideUp();
  });
});

